"""
Warm pool of companion selfies, generated by HeyGen.

HeyGen look generation is asynchronous (tens of seconds) and billed per look, so
we generate ahead into a pool instead of generating per request:

  top_up_pool()  - background job. Finds scenes that are thin and asks HeyGen
                   to generate looks for them.
  sync_pool()    - background job. Mirrors finished looks into Supabase Storage
                   and marks them ready.

The request path then just picks a ready image - instant, free, and always the
companion's real face because every image came from her trained HeyGen LoRA.

Bucket: companion-selfies (public)   Table: companion_selfies
Run scripts/selfie_pool_migration.sql once before using this.
"""
